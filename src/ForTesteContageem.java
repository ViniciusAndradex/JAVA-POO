
public class ForTesteContageem {

	public static void main(String[] args) {
		//contagem crescente
		for (int i = 0 ; i < 4 ; i++) {
			System.out.print(" - " + i + " - ");
		}
		System.out.println();
		//Contagem decrescente
		for (int i = 4 ; i > 0 ; i--) {
			System.out.print(" - " + i + " - ");
		}
		
	}

}
